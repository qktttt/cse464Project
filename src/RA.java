public class RA {
	double x;
	double y;
	double radius;

	/**
	 * Construct and initialize 
	 * @param x, x coordinate
	 * @param y, y coordinate
	 * @param radius, the radius that the restricted area has
	 */
	RA(double x, double y, double radius) {
		this.x = x;
		this.y = y;
		this.radius = radius;
	}
}
